piAmount = 3.14
circleRadius = 5
sideLengthA = 4
sideLengthB = 3
height = 5

print("In this task we are counting circle field")
circleField = (piAmount * (circleRadius ** 2))
print(circleField)

print("The second task we are circumference of the circle")
circumferenceField = (2 * piAmount * circleRadius)
print(circumferenceField)

print("In this task we are counting rectangle field")
rectangleField = (sideLengthA*sideLengthB)
print(rectangleField)

print("In this task we are counting trapezoid field")
trapezoidField = ((sideLengthA + sideLengthB) * height / 2)
print(trapezoidField)
